"""
Два игрока, Петя и Ваня, играют в следующую игру. Перед игроками лежит куча камней. Игроки ходят по очереди, первый ход делает Петя. 
За один ход игрок может добавить в кучу один или четыре камня либо увеличить количество камней в куче в три раза. 
Для того чтобы делать ходы, у каждого игрока есть неограниченное количество камней.
Игра завершается в тот момент, когда количество камней в куче становится не менее 67.
Победителем считается игрок, сделавший последний ход, т.е. первым получивший кучу, состоящую из 67 или более камней.
В начальный момент в куче было S камней; 1 ≤ S ≤ 66.
Будем говорить, что игрок имеет выигрышную стратегию, если он может выиграть при любых ходах противника.
Укажите такое значение S, при котором Петя не может выиграть за один ход, 
но при любом ходе Пети Ваня может выиграть своим первым ходом."""
def f(s,h):
    if s>=67 and h==2:
        return 1
    if s<67 and h==2:
        return 0
    if s>=67 and h==1:
        return 0
    if (h+1)%2==0:
        return f(s+1,h+1)or f(s+4,h+1) or f(s*3,h+1)
    else:
        return f(s+1,h+1)and f(s+4,h+1) and f(s*3,h+1)
for s in range(1,67):
    if f(s,0)==1:
        print(s)
        break


